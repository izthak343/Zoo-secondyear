package utilities;

public class Validators {
	/**
	 * Validates value is positive
	 * 
	 * @param value
	 *            - Value to check
	 * @return True if value is positive
	 */
	static public boolean IsPositive(double value) {
		return value >= 0;
	}

	/**
	 * Validates value is positive
	 * 
	 * @param value
	 *            - Value to check
	 * @return True if value is positive
	 */
	static public boolean IsPositive(int value) {
		return value >= 0;
	}

}
